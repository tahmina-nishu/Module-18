/***

Subtask-1:

Display sum of all the odd numbers from 91 to 129.

 */

let oddSum = 0;
for(let i = 91; i <= 129; i += 2)
{
    oddSum += i;
}
console.log('sum of all the odd numbers from 91 to 129 = ' + oddSum);
